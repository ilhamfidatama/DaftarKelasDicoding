package com.ilhamfidatama.DaftarKelasDicoding;

public class KelasDicoding {
    private String kelas, level, tipeKelas, link, gambar;

    public String getKelas() {
        return kelas;
    }

    public void setKelas(String kelas) {
        this.kelas = kelas;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getTipeKelas() {
        return tipeKelas;
    }

    public void setTipeKelas(String tipeKelas) {
        this.tipeKelas = tipeKelas;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }
}
